-- 4. TRIGGERS UNTUK SUPABASE AUTH (SYNC EMAIL)
-- --------------------------------------------------------

-- Function to sync profile email from auth.users
CREATE OR REPLACE FUNCTION public.handle_sync_profile_email()
RETURNS TRIGGER AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    INSERT INTO public.profiles (id, email)
    VALUES (NEW.id, NEW.email);
    RETURN NEW;
  ELSIF (TG_OP = 'UPDATE') THEN
    UPDATE public.profiles
    SET email = NEW.email
    WHERE id = NEW.id;
    RETURN NEW;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger Sinkronisasi Profil
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT OR UPDATE OF email ON auth.users
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_sync_profile_email();

-- --------------------------------------------------------
-- 5. TRIGGERS UNTUK JADWAL MENGAJAR
-- --------------------------------------------------------

-- Function to automatically calculate day_of_week from date
CREATE OR REPLACE FUNCTION public.set_hari_dari_tanggal()
RETURNS TRIGGER AS $$
BEGIN
   IF NEW.tanggal IS NOT NULL THEN
      NEW.hari := EXTRACT(ISODOW FROM NEW.tanggal);
   END IF;
   RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger Sinkronisasi Jadwal
DROP TRIGGER IF EXISTS trg_jadwal_set_hari ON public.jadwal_mengajar;
CREATE TRIGGER trg_jadwal_set_hari
  BEFORE INSERT OR UPDATE ON public.jadwal_mengajar
  FOR EACH ROW
  EXECUTE FUNCTION public.set_hari_dari_tanggal();

-- --------------------------------------------------------
-- 6. TRIGGERS UNTUK JURNAL HARIAN (CEK IS_TELAT)
-- --------------------------------------------------------

-- Jika tanggal submit (CURRENT_DATE) dengan tanggal kelas (tanggal) > 3 hari, flag is_telat = true
CREATE OR REPLACE FUNCTION public.set_is_telat_jurnal()
RETURNS TRIGGER AS $$
BEGIN
   IF (CURRENT_DATE - NEW.tanggal) > 3 THEN
      NEW.is_telat := true;
   ELSE
      NEW.is_telat := false;
   END IF;
   RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger Telat Jurnal
DROP TRIGGER IF EXISTS trg_jurnal_set_telat ON public.jurnal_harian;
CREATE TRIGGER trg_jurnal_set_telat
  BEFORE INSERT ON public.jurnal_harian
  FOR EACH ROW
  EXECUTE FUNCTION public.set_is_telat_jurnal();

