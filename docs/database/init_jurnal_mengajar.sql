-- --------------------------------------------------------
-- 1. BUAT TABEL PROFIL & MASTER DATA
-- --------------------------------------------------------

-- Tabel Profiles
CREATE TABLE public.profiles (
    id uuid NOT NULL PRIMARY KEY, -- References auth.users(id)
    email text,
    nama_lengkap text,
    role text CHECK (role IN ('admin', 'guru')),
    jabatan text,
    alamat text,
    no_telp text,
    foto_url text
);

-- Master Kelas
CREATE TABLE public.master_kelas (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_kelas text NOT NULL UNIQUE
);

-- Master Mata Pelajaran
CREATE TABLE public.master_mata_pelajaran (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_mata_pelajaran text NOT NULL UNIQUE,
    created_at timestamp with time zone DEFAULT now()
);

-- Master Periode Ajaran
CREATE TABLE public.master_periode (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_periode text NOT NULL,
    is_active boolean DEFAULT false
);

-- Master Jam / Waktu Pelajaran
CREATE TABLE public.master_jam (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    jam_ke integer NOT NULL UNIQUE,
    waktu_reguler text NOT NULL,
    waktu_puasa text NOT NULL,
    created_at timestamp with time zone DEFAULT now()
);

-- Master Siswa
CREATE TABLE public.master_siswa (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_siswa text NOT NULL,
    nisn text,
    kelas_id bigint NOT NULL REFERENCES public.master_kelas(id) ON DELETE CASCADE
);


-- --------------------------------------------------------
-- 2. BUAT TABEL TRANSAKSIONAL (JADWAL, JURNAL, PRESENSI)
-- --------------------------------------------------------

-- Jadwal Mengajar
CREATE TABLE public.jadwal_mengajar (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    guru_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
    periode_id bigint REFERENCES public.master_periode(id) ON DELETE CASCADE,
    kelas_id bigint REFERENCES public.master_kelas(id) ON DELETE CASCADE,
    mata_pelajaran_id bigint REFERENCES public.master_mata_pelajaran(id) ON DELETE CASCADE,
    jam_id bigint REFERENCES public.master_jam(id) ON DELETE CASCADE,
    hari integer CHECK (hari >= 1 AND hari <= 7), -- Calculated via trigger
    tanggal date,
    is_active boolean DEFAULT true
);

-- Jurnal Harian
CREATE TABLE public.jurnal_harian (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    jadwal_id bigint NOT NULL REFERENCES public.jadwal_mengajar(id) ON DELETE CASCADE,
    tanggal date DEFAULT CURRENT_DATE,
    materi text NOT NULL,
    catatan text,
    foto_lampiran_url text,
    is_telat boolean DEFAULT false,
    status text DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'rejected')),
    catatan_admin text,
    validated_by uuid REFERENCES public.profiles(id),
    validated_at timestamp with time zone,
    created_at timestamp with time zone DEFAULT now()
);

-- Presensi Siswa
CREATE TABLE public.presensi_siswa (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    jurnal_id bigint NOT NULL REFERENCES public.jurnal_harian(id) ON DELETE CASCADE,
    siswa_id bigint NOT NULL REFERENCES public.master_siswa(id) ON DELETE CASCADE,
    kelas_id bigint REFERENCES public.master_kelas(id),
    status text DEFAULT 'Hadir' CHECK (status IN ('Hadir', 'Sakit', 'Izin', 'Alpha', 'H', 'S', 'I', 'A'))
);


